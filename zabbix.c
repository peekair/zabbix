__int64 zabbix_crypt_init()
{
	__int64 result;

	g_flag_0x00 = 0;
	g_flag_0x01 = 0;
	g_flag_0x02 = 0;
	do
	{
		g_table[g_flag_0x00] = g_flag_0x00;
		++g_flag_0x00;
		result = g_flag_0x00;
	}
	while ( g_flag_0x00 );
	return result;
}

